<h1>Biography</h1>

    <p>Phillip Dennis "Phil" Ivey Jr. (born February 1, 1976) is an American professional poker player who has won nine World Series of Poker bracelets, one World Poker Tour title and appeared at nine World Poker Tour final tables.
    Ivey is regarded by numerous poker observers and contemporaries as the best all-round player in the world today.</p>
